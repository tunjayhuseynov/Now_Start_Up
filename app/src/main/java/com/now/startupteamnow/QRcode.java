package com.now.startupteamnow;

public class QRcode {

    private String Code;
    private int Bonus;
    private float xCoordination;
    private float yCoordination;
    private String CompanyName;
    private String Details;
    private boolean BonusType;
    private String Image;

    public String getCode() {
        return Code;
    }

    public int getBonus() {
        return Bonus;
    }

    public float getxCoordination() {
        return xCoordination;
    }

    public float getyCoordination() {
        return yCoordination;
    }

    public String getCompanyName() {
        return CompanyName;
    }

    public String getDetails() {
        return Details;
    }

    public boolean isBonusType() {
        return BonusType;
    }

    public String getImage() {
        return Image;
    }
}
